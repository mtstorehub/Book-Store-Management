    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(tbMembers.getSelectedRow()>-1){
        int i=tbMembers.getSelectedRow();
        try{
            createDB();
                String selectSql="select * from Member limit "+i+",1 ";
                stmt=conn.createStatement();
                rs=stmt.executeQuery(selectSql);
                rs.next();
                String email=rs.getString("Email");
                String delSql="delete from Member where Email = ?";
                
                pstmt=conn.prepareStatement(delSql);
            pstmt.setString(1,email);
            
            if(pstmt.executeUpdate()==1){
                JOptionPane.showMessageDialog(this,"Deleted Successfully ");
                model.removeRow(i);
                
            }
            else{
                JOptionPane.showMessageDialog(this,"Deleted Failed");
            }
            stmt.close();
            conn.close();
           
                
            } catch (SQLException ex) {
                Logger.getLogger(ManageBooks.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Select data");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

